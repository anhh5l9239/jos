int
sys_fork(void)
{
  return fork();
}

int
sys_exit(void)
{
  exit();
  return 0;  // not reached
}

int
sys_wait(void)
{
  return wait();
}

int
sys_getpid(void)
{
  return proc->pid;
}

// return how many clock tick interrupts have occurred
// since start.
int
sys_uptime(void)
{
  uint xticks;
  
  acquire(&tickslock);
  xticks = ticks;
  release(&tickslock);
  return xticks;
}

int
sys_print(void)
{
	char *str;
	if(argstr(0, &str) < 0)
		return -1;

	cprintf(str, cpu->id, proc->pid);
	return 0;
}

